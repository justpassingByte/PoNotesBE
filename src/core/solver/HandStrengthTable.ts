const ALL_HANDS = HandClassGenerator.generateAll();
for (const hand of ALL_HANDS) {
    if (HAND_STRENGTH[hand] === undefined) {
        throw new Error(`HAND_STRENGTH missing value for hand class: ${hand}`);
    }
}

if (Object.keys(HAND_STRENGTH).length !== ALL_HANDS.length) {
    throw new Error(
        `HAND_STRENGTH must define exactly ${ALL_HANDS.length} hand classes, got ${Object.keys(HAND_STRENGTH).length}`
    );
}

for (const hand of ALL_HANDS) {
    const value = HAND_STRENGTH[hand];
    if (!Number.isFinite(value) || value < 0 || value > 10) {
        throw new Error(`HAND_STRENGTH value out of range [0,10] for ${hand}: ${value}`);
    }
}
